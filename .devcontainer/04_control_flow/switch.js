// switch case statement

const val = 5

switch(val){
    case 1:
        console.log("This is Jan")
        break;
    case 2:
        console.log("This is Feb")
        break;
    case 3:
        console.log("This is March")
        break;
    case 4:
        console.log("This is April")
        break;
    case 5:
        console.log("This is May")
        break;
    default:
        console.log("This is Default")
        break;            
        
}